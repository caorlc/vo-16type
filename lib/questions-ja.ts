export function calcMBTI(answers: Record<number, "A" | "B">) {
  const count = (ids: number[]) => {
    let a = 0, b = 0
    ids.forEach((id: number) => {
      if (answers[id] === "A") a++
      if (answers[id] === "B") b++
    })
    return { a, b }
  }

  const ei = count(EI)
  const sn = count(SN)
  const tf = count(TF)
  const jp = count(JP)

  const type =
    (ei.a > ei.b ? "E" : "I") +
    (sn.a > sn.b ? "S" : "N") +
    (tf.a > tf.b ? "T" : "F") +
    (jp.a > jp.b ? "J" : "P")

  return {
    type,
    detail: { EI: ei, SN: sn, TF: tf, JP: jp }
  }
} 